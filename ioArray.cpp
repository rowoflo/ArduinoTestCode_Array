template <typename U>
std::ostream& operator<<(std::ostream& ostreamObj, const ioArray<U>& rhsObj)
{
	int iElem;
	
	// Loop through elements
	for (iElem = 0; iElem < rhsObj._length; iElem++)
	{
		ostreamObj << rhsObj._elements[iElem] << "\n";
	}
	return (ostreamObj);
}

